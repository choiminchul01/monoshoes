// 색상 이름을 hex 코드로 변환하는 유틸리티
export const getColorHex = (colorName: string): string => {
    const colorMap: { [key: string]: string } = {
        // 기본 색상
        '블랙': '#000000',
        '화이트': '#FFFFFF',
        '베이지': '#F5F5DC',
        '그레이': '#808080',
        '네이비': '#000080',
        '브라운': '#8B4513',
        '카키': '#C3B091',

        // 밝은 색상
        '레드': '#FF0000',
        '핑크': '#FFC0CB',
        '오렌지': '#FFA500',
        '옐로우': '#FFFF00',
        '그린': '#008000',
        '블루': '#0000FF',
        '퍼플': '#800080',

        // 파스텔 톤
        '아이보리': '#FFFFF0',
        '크림': '#FFFDD0',
        '라벤더': '#E6E6FA',
        '민트': '#98FF98',
        '피치': '#FFE5B4',

        // 다크 톤
        '차콜': '#36454F',
        '와인': '#722F37',
        '버건디': '#800020',
        '올리브': '#808000',
    };

    // 입력된 이름에서 공백 제거하고 소문자로 변환
    const normalizedName = colorName.trim();

    // 매칭되는 색상이 있으면 반환, 없으면 기본적으로 블랙 반환
    return colorMap[normalizedName] || '#000000';
};

